namespace BundleTransformer.CleanCss
{
	/// <summary>
	/// Special comments mode
	/// </summary>
	public enum SpecialCommentsMode
	{
		/// <summary>
		/// Keep all special comments
		/// </summary>
		KeepAll = 0,

		/// <summary>
		/// Keep first one special comment
		/// </summary>
		KeepFirstOne,

		/// <summary>
		/// Remove all special comments
		/// </summary>
		RemoveAll
	}
}